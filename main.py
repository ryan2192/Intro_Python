# Area Calculator
#
import math

# Triangle 
base = 20
height = 30
area = base * height / 2
print("The triangle area is ", area)

# Rectangle
length = 2
width = 12
area = length * width
print("The rectangle area is ", area)

# Circle
radius = 36
area = math.pi * radius * radius
print("The circle area is ", area)
